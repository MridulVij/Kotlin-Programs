// Data Types in Kotlin

/*Data types are divided into different groups:

Numbers - int
Characters - char
Booleans - true, false
Strings - "Mridul"
Arrays -
*/

fun main() {
    // By Default Variables automatically accepted
    val myInt = 10;
    val myDouble = 10.0001;
    val myChar = 'M';
    val myBool = true;
    val myText = "Hello";


    // By Specific Variables Declaration - hame hamare hisab sai deciade krna h ki konsa variable kis data type ka hoga
    val myint: Int = 10;
    val myfloat: Float = 10.20000000023F;
    val myshort: Short = 9999;
    val mylong: Long = 999999999999999999L;
    val mydouble: Double = 10.00001;
    val myletter: String = "Mridul";
    val mybool: Boolean = true;
    val mychar: Char = 'M';


}